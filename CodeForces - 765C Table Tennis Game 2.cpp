#include <stdio.h>
int main() {
  int k, a, b;
  while (scanf("%d%d%d", &k, &a, &b) != EOF) {
    if (a % k && b / k == 0 || b % k && a / k == 0)
      printf("-1\n");
    else
      printf("%d\n", a / k + b / k);
  }
}
